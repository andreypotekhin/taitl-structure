import shutil
from contextlib import contextmanager
from pathlib import Path
from uuid import uuid4

from structure.app.target.pyspark.api import pyspark


@contextmanager
def workspace_tmp():
    root = Path(".pytest-workspace-tmp") / uuid4().hex
    root.mkdir(parents=True)
    try:
        yield root
    finally:
        shutil.rmtree(root, ignore_errors=True)


def test_v1_write_generated_files_adds_modifies_and_preserves_unchanged_files() -> None:
    with workspace_tmp() as root:
        _assert_write_generated_files_adds_modifies_and_preserves_unchanged_files(root)


def _assert_write_generated_files_adds_modifies_and_preserves_unchanged_files(root: Path) -> None:
    first = pyspark.files.write()(
        {
            "pkg/a.py": "# Generated by Structure. Do not edit by hand.\n# Source: a\n\nA = 1\n",
            "pkg/b.py": "# Generated by Structure. Do not edit by hand.\n# Source: b\n\nB = 1\n",
        },
        root=root,
    )

    second = pyspark.files.write()(
        {
            "pkg/a.py": "# Generated by Structure. Do not edit by hand.\n# Source: a\n\nA = 2\n",
            "pkg/b.py": "# Generated by Structure. Do not edit by hand.\n# Source: b\n\nB = 1\n",
        },
        root=root,
    )

    assert first.count("added") == 2
    assert second.count("modified") == 1
    assert second.count("unchanged") == 1
    assert (root / "pkg/a.py").read_text(encoding="utf-8").endswith("A = 2\n")


def test_v1_compare_generated_files_reports_fail_on_diff_statuses() -> None:
    with workspace_tmp() as root:
        _assert_compare_generated_files_reports_fail_on_diff_statuses(root)


def _assert_compare_generated_files_reports_fail_on_diff_statuses(root: Path) -> None:
    pyspark.files.write()(
        {
            "pkg/a.py": "# Generated by Structure. Do not edit by hand.\n# Source: a\n\nA = 1\n",
            "pkg/old.py": "# Generated by Structure. Do not edit by hand.\n# Source: old\n\nOLD = 1\n",
        },
        root=root,
    )

    result = pyspark.files.compare()(
        {
            "pkg/a.py": "# Generated by Structure. Do not edit by hand.\n# Source: a\n\nA = 2\n",
            "pkg/new.py": "# Generated by Structure. Do not edit by hand.\n# Source: new\n\nNEW = 1\n",
        },
        root=root,
    )
    statuses = {change.path: change.status for change in result.changes}

    assert statuses == {
        "pkg/a.py": "modified",
        "pkg/new.py": "added",
        "pkg/old.py": "removed",
    }
    assert result.changed()


def test_v1_compare_generated_files_ignores_unowned_files() -> None:
    with workspace_tmp() as root:
        _assert_compare_generated_files_ignores_unowned_files(root)


def _assert_compare_generated_files_ignores_unowned_files(root: Path) -> None:
    target = root / "pkg/manual.py"
    target.parent.mkdir(parents=True)
    target.write_text("print('owned by the user')\n", encoding="utf-8")

    result = pyspark.files.compare()({}, root=root)

    assert result.changes == ()
