from __future__ import annotations

from pathlib import Path

from structure.app.configuration.model.StructureConfig import StructureConfig


class CleanGeneratedFiles:

    def __call__(self, config: StructureConfig) -> tuple[str, ...]:
        if not config.generated_dir.exists():
            return ("Structure clean passed", "  removed files: 0")
        removed = 0
        for path in sorted(item for item in config.generated_dir.rglob("*") if item.is_file()):
            if self._owned(path):
                path.unlink()
                removed += 1
        for path in sorted((item for item in config.generated_dir.rglob("*") if item.is_dir()), reverse=True):
            try:
                path.rmdir()
            except OSError:
                pass
        return ("Structure clean passed", f"  removed files: {removed}")

    def _owned(self, path: Path) -> bool:
        try:
            text = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            return False
        if text.startswith("# Generated by Structure. Do not edit by hand."):
            return True
        return path.suffix == ".json" and '"source_transform"' in text and '"generated_transform_class"' in text
