from __future__ import annotations


class RenderPySparkRuntimeModule:

    def __call__(self) -> str:
        return "\n".join(
            [
                "from pyspark.sql import functions as F",
                "from types import MappingProxyType",
                "from collections.abc import Mapping",
                "",
                "",
                'def assert_schema(df, schema, *, name: str, mode: str) -> None:',
                "    actual = df.schema",
                "    actual_names = set(actual.fieldNames())",
                "    expected_names = {field.name for field in schema}",
                "    missing = expected_names - actual_names",
                "    if missing:",
                '        names = ", ".join(sorted(missing))',
                '        raise ValueError(f"{name} is missing required column(s): {names}")',
                '    if mode == "strict":',
                "        extra = actual_names - expected_names",
                "        if extra:",
                '            names = ", ".join(sorted(extra))',
                '            raise ValueError(f"{name} has unexpected column(s): {names}")',
                "    for expected in schema:",
                "        actual_field = actual[expected.name]",
                "        if actual_field.dataType != expected.dataType:",
                '            raise ValueError(f"{name}.{expected.name} expected {expected.dataType}, got {actual_field.dataType}")',
                "",
                "",
                "def project_schema(df, schema):",
                "    return df.select(*(F.col(field.name).cast(field.dataType).alias(field.name) for field in schema))",
                "",
                "",
                "class HookInputs:",
                "",
                "    def __init__(self, **inputs):",
                '        object.__setattr__(self, "_structure_frozen", False)',
                '        object.__setattr__(self, "_structure_names", tuple(inputs))',
                "        for name, value in inputs.items():",
                "            object.__setattr__(self, name, value)",
                '        object.__setattr__(self, "_structure_frozen", True)',
                "",
                "    def __setattr__(self, name, value):",
                '        if getattr(self, "_structure_frozen", False):',
                '            raise AttributeError("HookInputs is read-only")',
                "        object.__setattr__(self, name, value)",
                "",
                "",
                "class TransformResult:",
                "",
                "    def __init__(self, outputs, *, single=False, schema=None):",
                "        values = dict(outputs)",
                "        if single:",
                "            if len(values) != 1:",
                '                raise ValueError("single-output TransformResult requires exactly one output")',
                "        object.__setattr__(self, '_structure_outputs', MappingProxyType(values))",
                "        object.__setattr__(self, 'schema', ResultSchemas(schema))",
                "",
                "    def __getitem__(self, name):",
                "        return self._structure_outputs[name]",
                "",
                "    def __iter__(self):",
                "        return iter(self._structure_outputs)",
                "",
                "    def __len__(self):",
                "        return len(self._structure_outputs)",
                "",
                "    def __getattr__(self, name):",
                "        if name in self._structure_outputs:",
                "            return self._structure_outputs[name]",
                "        raise AttributeError(name)",
                "",
                "    def __setattr__(self, name, value):",
                '        raise AttributeError("TransformResult is read-only")',
                "",
                "    def as_dict(self):",
                "        return dict(self._structure_outputs)",
                "",
                "",
                "class ResultSchemas(Mapping):",
                "",
                "    def __init__(self, schemas=None):",
                "        object.__setattr__(self, '_schemas', MappingProxyType(dict(schemas or {})))",
                "",
                "    def __getitem__(self, name):",
                "        return self._schemas[name]",
                "",
                "    def __iter__(self):",
                "        return iter(self._schemas)",
                "",
                "    def __len__(self):",
                "        return len(self._schemas)",
                "",
                "    def __getattr__(self, name):",
                "        if name in self._schemas:",
                "            return self._schemas[name]",
                "        raise AttributeError(name)",
                "",
                "    def __setattr__(self, name, value):",
                '        raise AttributeError("ResultSchemas is read-only")',
                "",
                "    def as_dict(self):",
                "        return dict(self._schemas)",
                "",
            ]
        )


render_pyspark_runtime_module = RenderPySparkRuntimeModule()
